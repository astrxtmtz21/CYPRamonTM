X1 = float(input("Coordenada del punto P1 en X:"))
Y1 = float(input("Coordenada del punto P1 en Y:"))
X2 = float(input("Coordenada del punto P2 en X:"))
Y2 = float(input("Coordenada del punto P2 en Y:"))
DIS = ((X1 - X2) ** 2 + (Y1 - Y2) ** 2) ** 0.5
print(f"La distancia resultante entre los dos puntos es { DIS } dadas dichas coordenadas")
